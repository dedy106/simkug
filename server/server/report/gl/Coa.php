	function getTotalPage()
	{
		//$sql = "select count(*) from glma a ".
		//		" inner join masakun b on b.kode = a.kode and a.kode_lokasi = b.kode_lokasi ". $this->filter;
		$sql="select count(*) from masakun ".$this->filter;
		global $dbLib;
		$rs = $dbLib->execute($sql);		
		$totPage = 0;
		if ($rs)
		{
			$count = $rs->fields[0];
			$totPage = ceil($count / $this->rows);		
		}
		return $totPage;
	}
	function getHtml()
	{
		$sql = "select kode_akun,nama,modul,jenis,kode_curr,block,akun_konsol,kode_flag,kode_lokasi from masakun";
		
		global $dbLib;
		$start = (($this->page-1) * $this->rows);
		$rs=$dbLib->LimitQuery($sql,$this->rows,$start);	
		$i = 1;
		$html="";
		
		while ($row = $rs->FetchNextObject($toupper=false))
		{
		    
			$html.=$row->kode_akun;
			$rs->MoveNext();
			$i=$i+1;
		}
		
		
		uses("server_report_GL_gen_coa", false);
		return $html;
	}
