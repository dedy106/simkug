			var total = 0;
			var strSQL = "select a.keterangan,a.nama_rek,a.no_rek,a.bank,a.cabang,a.jenis,a.nilai from yk_spb_d a where a.no_spb='"+this.e_nb.getText()+"' and kode_lokasi='"+this.app._lokasi+"'";
