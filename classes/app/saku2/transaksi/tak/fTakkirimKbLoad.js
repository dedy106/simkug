	doAfterUpload: function(sender, result, data){		
	    try{   			
			this.dataUpload = data;
			if (result) {								
				this.sg1.clear();				
				this.selectPage(undefined, 1);
				this.sgn.setTotalPage(Math.ceil(this.dataUpload.rows.length / 20));
				this.sgn.rearrange();
				this.sgn.activePage = 0;	
				this.totTrans = 0;
				for (var j=0; j < this.dataUpload.rows.length;j++){
					line1 = this.dataUpload.rows[j];
					this.totTrans += parseFloat(line1.nilai);					
				}
				this.e_trans.setText(floatToNilai(this.totTrans));	
			}else throw(data);					
   		}catch(e){
   		   this.sg1.hideLoading();
   		   systemAPI.alert(e,"Proses Berhenti.");
        }
	},
	selectPage: function(sender,page){
		var start = (page - 1) * 20;
		var finish = start + 20;
		finish = (finish > this.dataUpload.rows.length ? this.dataUpload.rows.length : finish);
		this.sg1.clear();
		for (var i=start; i < finish;i++){
			line = this.dataUpload.rows[i];			
			this.sg1.appendData([line.mitra,line.bank,line.cabang,line.no_rek,line.nama_rek,floatToNilai(line.nilai),line.keterangan]);
		}
		this.sg1.setNoUrut(start);
	},
